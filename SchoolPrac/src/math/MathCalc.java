public class MathCalc {
    //static methods only 
    
    public static int power(int num1, int num2){ 
        return (int) Math.pow(num1, num2); 
}
    public static int multiply(int num1, int num2){ 
        return num1*num2; 
}
    public static int divide(int num1, int num2){ 
        return num1/num2; 
}
    
   
    
}
